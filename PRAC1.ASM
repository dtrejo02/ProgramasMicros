			CLRF		TRISC
			BCF			TRISB,7
			BCF			STATUS,RP0	;B0

INICIO		BTFSS		PORTB,0
			GOTO		INICIO
			MOVF		PORTD,W
			MOVWF		0X20	;VALOR1 O A
			MOVWF		PORTC

PUSHES		BTFSC		PORTB,1
			GOTO		SUMA
			BTFSC		PORTB,2
			GOTO		RESTA
			BTFSC		PORTB,3
			GOTO 		MULTI
			GOTO		PUSHES

SUMA		MOVF		PORTD,W
			ADDWF		0X20,W
			MOVWF		PORTC
			BTFSC		STATUS,C
			BSF			PORTB,7
			GOTO		DIV

RESTA		MOVF		PORTD,W
			SUBWF		0X20,W
			BTFSS		STATUS,C
			SUBLW		0X00
			MOVWF		PORTC
			BTFSS		STATUS,C
			BSF			PORTB,7
			GOTO		DIV

MULTI		MOVLW		0XFF
			ANDWF		0X20,W	;A
			BTFSS		STATUS,Z
			GOTO		TESTB
			MOVLW		0X00
			MOVWF		PORTC
			GOTO		DIV
TESTB		MOVLW		0XFF
			ANDWF		PORTD,W	;B
			BTFSS		STATUS,Z
			GOTO		MULTBUCLE
			MOVLW		0X00
			MOVWF		PORTC
			GOTO		DIV
MULTIBUCLE	MOVF		PORTD,W
			MOVWF		0X21	;VALOR2 O B
			MOVLW		0X00
			MOVWF		0X22	;VALOR D
DECMULT		DECFSZ		0X21,F
			GOTO		BUCLEMULT
			MOVF		0X22,W
			MOVWF		PORTC
			BTFSC		STATUS,C
			BSF			PORTB,7
			GOTO		DIV
BUCLEMULT	MOVF		0X20,W
			ADDWF		0X22,F
			GOTO		DECMULT

DIV			BCF			PORTB,7
			MOVF		PORTD,W
			MOVWF		0X21	;VALOR2 O B
			MOVLW		0X00
			MOVWF		0X22	;CONTADOR CNT
			MOVLW		0XFF
			ANDWF		0X20,W
			BTFSS		STATUS,Z
			GOTO		TESTDIVB
			MOVF		0X22,W
			MOVWF		PORTC
TESTDIVB	MOVLW		0XFF
			ANDWF		0X21,W
			BTFSS		STATUS,Z
			GOTO		DIVA
			MOVLW		0XFF
			MOVWF		PORTC
			BSF			PORTB,7
DIVA		INCFSZ		0X21,F
			GOTO		BUCLEMULT
			MOVF		0X22,W
			MOVWF		PORTC
			BTFSC		STATUS,C
			BSF			PORTB,7
			GOTO		DIV
BUCLEMULT	MOVF		0X20,W
			ADDWF		0X22,F
			GOTO		DECMULT
			
			END